function inject (brainrots) {
    const container = document.querySelector('.container');
    brainrots.forEach(brainrots => {
        container.insertAdjacentHTML('beforeend', `
        <div class="card" data-type="${brainrots.Rarity}">
            <h2 class="card-name">${brainrots.name}</h2>
            <img class="card-img" src="${brainrots.Image}" alt="${brainrots.alt}" />
            <h3 class="card-price">$${brainrots.price}</h3>
            <button class="btn">Click to Buy</button>
        </div>
        `)
        })
    }
    inject(brainrots);
function bought() {
    const buttons = document.querySelectorAll(".btn");
    const btnArr = Array.from(buttons);
    btnArr.forEach((button) => {
        button.addEventListener("click", function (event) {
            event.target.textContent = `Added to Cart!`;
            event.target.style.backgroundColor = "green";
            setTimeout(() => {
                event.target.textContent = "Click to Buy";
                event.target.style.backgroundColor = "";
            }, 1000);
        });
    });
}
bought();
function cart() {
    const buttons = document.querySelectorAll(".btn");
    const cartItemsContainer = getCartItemsContainer();

    buttons.forEach(btn => {
        btn.addEventListener("click", (event) => {
            const card = event.target.closest(".card");
            const name = card.querySelector(".card-name").textContent;
            const price = card.querySelector(".card-price").textContent;
            cartItemsContainer.insertAdjacentHTML(
                "afterend", `
                <div class="cart-item">
                    <h4 class="cart-item-name">${name}</h4>
                    <div class="cart-item-price">${price}</div>
                </div>
            `);
        });
    });
}
function totalPrice() {
    const cart = document.querySelector(".cart");
    const buttons = document.querySelectorAll(".btn");
    const cartItemsContainer = getCartItemsContainer();
    const totalDisplay = document.createElement("div");
    totalDisplay.className = "total-price";
    totalDisplay.innerHTML = `<h3>Total Price: $0.00</h3>`;
    cartItemsContainer.appendChild(totalDisplay);
    let total = 0;
    buttons.forEach(btn => {
        btn.addEventListener("click", (event) => {
            const card = event.target.closest(".card");
            const pricetext = card.querySelector(".card-price").textContent;
            const price = parseFloat(pricetext.replace('$', '').replace(/,/g, ''));
            total += price;
            totalDisplay.innerHTML = `<h3>Total Price: $${total}</h3>`;
        });
    })}
totalPrice();
function getCartItemsContainer() {
    const cart = document.querySelector(".cart");
    let items = cart.querySelector(".cart-items");
    if (!items) {
        items = document.createElement("div");
        items.className = "cart-items";
        cart.appendChild(items);
    }
    return items;
}
cart();
function filterByRarity(rarity) {
    const brainrotanimals = document.querySelectorAll('.card');
    brainrotanimals.forEach(animal => {
        const raritytype = animal.getAttribute('data-type');
        if ( raritytype === rarity ){
            animal.style.display = '';
        } else {
            animal.style.display = 'none';
        }
    })};
function showAll() {
    const brainrotanimals = document.querySelectorAll('.card');
    brainrotanimals.forEach(animal => {
        animal.style.display = '';
    });
}
